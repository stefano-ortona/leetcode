  public int getNonOverlappingClaim() {
    final List<FabricClaim> allClaims = readInput();
    final int[][] occupied = createInitialGrid(allClaims);
    // fill the grid
    fillTheGrid(allClaims, occupied);
    // re-run trhough all the claims and get the first non overlapping
    for (final FabricClaim oneClaim : allClaims) {
      boolean isNonOverlapping = true;
      for (int i = oneClaim.leftCornerRow; (i < (oneClaim.leftCornerRow + oneClaim.height)) && isNonOverlapping; i++) {
        for (int j = oneClaim.leftCornerColumn; (j < (oneClaim.leftCornerColumn + oneClaim.width))
            && isNonOverlapping; j++) {
          isNonOverlapping &= occupied[i][j] == 1;
        }
      }
      if (isNonOverlapping) {
        return oneClaim.id;
      }
    }
    return -1;
  }

  public int getOverlappingSquares() {
    final List<FabricClaim> allClaims = readInput();
    final int[][] occupied = createInitialGrid(allClaims);
    // fill the grid
    fillTheGrid(allClaims, occupied);
    // count square occupied by more than one
    int moreThanOneOccupied = 0;
    for (int i = 0; i < occupied.length; i++) {
      for (int j = 0; j < occupied[0].length; j++) {
        if (occupied[i][j] > 1) {
          moreThanOneOccupied++;
        }
      }
    }
    return moreThanOneOccupied;
  }

  private int[][] createInitialGrid(final List<FabricClaim> allClaims) {
    int curMaxRow = 0;
    int curMaxColumn = 0;
    for (final FabricClaim oneClaim : allClaims) {
      curMaxColumn = Math.max(curMaxColumn, oneClaim.leftCornerColumn + oneClaim.width + 1);
      curMaxRow = Math.max(curMaxRow, oneClaim.leftCornerRow + oneClaim.height + 1);
    }
    return new int[curMaxRow][curMaxColumn];
  }

  private void fillTheGrid(List<FabricClaim> allClaims, int[][] gridOccupied) {
    // fill the grid
    for (final FabricClaim oneClaim : allClaims) {
      for (int i = oneClaim.leftCornerRow; i < (oneClaim.leftCornerRow + oneClaim.height); i++) {
        for (int j = oneClaim.leftCornerColumn; j < (oneClaim.leftCornerColumn + oneClaim.width); j++) {
          gridOccupied[i][j]++;
        }
      }
    }
  }

